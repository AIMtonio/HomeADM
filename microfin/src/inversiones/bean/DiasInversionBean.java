package inversiones.bean;

public class DiasInversionBean {
	
	private String diaInversionID;
	private String tipoInvercionID;
	
	private int plazoInferior;
	private int plazoSuperior;
	
	public String getDiaInversionID() {
		return diaInversionID;
	}
	public void setDiaInversionID(String diaInversionID) {
		this.diaInversionID = diaInversionID;
	}
	public String getTipoInvercionID() {
		return tipoInvercionID;
	}
	public void setTipoInvercionID(String tipoInvercionID) {
		this.tipoInvercionID = tipoInvercionID;
	}
	public int getPlazoInferior() {
		return plazoInferior;
	}
	public void setPlazoInferior(int plazoInferior) {
		this.plazoInferior = plazoInferior;
	}
	public int getPlazoSuperior() {
		return plazoSuperior;
	}
	public void setPlazoSuperior(int plazoSuperior) {
		this.plazoSuperior = plazoSuperior;
	}
	
		

}
