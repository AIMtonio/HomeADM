package cliente.bean;

public class RepExtravioDocsBean {
	
	private String clienteID;
	private String tipoRep;
	private String cuentaID;
	private String inversionID;
	private String nombreCliente;
	
	public String getClienteID() {
		return clienteID;
	}
	public void setClienteID(String clienteID) {
		this.clienteID = clienteID;
	}
	public String getTipoRep() {
		return tipoRep;
	}
	public void setTipoRep(String tipoRep) {
		this.tipoRep = tipoRep;
	}
	public String getCuentaID() {
		return cuentaID;
	}
	public void setCuentaID(String cuentaID) {
		this.cuentaID = cuentaID;
	}
	public String getInversionID() {
		return inversionID;
	}
	public void setInversionID(String inversionID) {
		this.inversionID = inversionID;
	}
	public String getNombreCliente() {
		return nombreCliente;
	}
	public void setNombreCliente(String nombreCliente) {
		this.nombreCliente = nombreCliente;
	}
}
