package tesoreria.bean;

public class CatTipoCajeroBean {

	private String tipoCajeroID;
	private String descripcion;
	private String claveCnbv;
	
	
	public String getTipoCajeroID() {
		return tipoCajeroID;
	}
	public void setTipoCajeroID(String tipoCajeroID) {
		this.tipoCajeroID = tipoCajeroID;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getClaveCnbv() {
		return claveCnbv;
	}
	public void setClaveCnbv(String claveCnbv) {
		this.claveCnbv = claveCnbv;
	}
	
	
	
}
