	public CuentasMayorMonBean consultaPrincipal(CuentasMayorMonBean cuentasMayorMonBean, int tipoConsulta){
		String query = "call CUENTASMAYORMONCON(?,?,?,?,?,?,?,?,?);";
		Object[] parametros = { 
				cuentasMayorMonBean.getConceptoMonID(),
				tipoConsulta,
				
				Constantes.ENTERO_CERO,
				Constantes.ENTERO_CERO,
				Constantes.FECHA_VACIA,
				Constantes.STRING_VACIO,
				"CuentasMayorMonDAO.consultaPrincipal",
				Constantes.ENTERO_CERO,
				Constantes.ENTERO_CERO
				};
		loggerSAFI.info(parametrosAuditoriaBean.getOrigenDatos()+"-"+"call CUENTASMAYORMONCON(" + Arrays.toString(parametros) +")");
		List matches= ((JdbcTemplate)conexionOrigenDatosBean.getOrigenDatosMapa().get(parametrosAuditoriaBean.getOrigenDatos())).query(query,parametros  ,new RowMapper() {
			public Object mapRow(ResultSet resultSet, int rowNum) throws SQLException {
				CuentasMayorMonBean cuentasMayorMon = new CuentasMayorMonBean();
				cuentasMayorMon.setConceptoMonID(resultSet.getString(1));
				cuentasMayorMon.setCuenta(resultSet.getString(2));
				cuentasMayorMon.setNomenclatura(resultSet.getString(3));
				cuentasMayorMon.setNomenclaturaCR(resultSet.getString(4));
				return cuentasMayorMon;
			}
		});
		return matches.size() > 0 ? (CuentasMayorMonBean) matches.get(0) : null;
	}

	
