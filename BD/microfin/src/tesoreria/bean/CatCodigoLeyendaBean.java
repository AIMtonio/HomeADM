package tesoreria.bean;

public class CatCodigoLeyendaBean {
	
	private String codigoLeyenda;
	private String Identificador;
	private String Descripcion;
	private String TipoDeposito;
	public String getCodigoLeyenda() {
		return codigoLeyenda;
	}
	public void setCodigoLeyenda(String codigoLeyenda) {
		this.codigoLeyenda = codigoLeyenda;
	}
	public String getIdentificador() {
		return Identificador;
	}
	public void setIdentificador(String identificador) {
		Identificador = identificador;
	}
	public String getDescripcion() {
		return Descripcion;
	}
	public void setDescripcion(String descripcion) {
		Descripcion = descripcion;
	}
	public String getTipoDeposito() {
		return TipoDeposito;
	}
	public void setTipoDeposito(String tipoDeposito) {
		TipoDeposito = tipoDeposito;
	}
	
	
}
