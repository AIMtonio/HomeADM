	public SubCtaSucursDivBean consultaSubCtaSucursal(SubCtaSucursDivBean subCtaSucursDivBean, int tipoConsulta) {
		SubCtaSucursDivBean consultaBean = null;
		try{
			// Query con el Store Procedure
			String query = "call SUBCTASUCURSDIVCON(?,?,?,?,?,   ?,?,?,?,?);";
			Object[] parametros = { subCtaSucursDivBean.getConceptoMonID(),
									subCtaSucursDivBean.getSucursalID(),
									tipoConsulta,

									Constantes.ENTERO_CERO,
									Constantes.ENTERO_CERO,
									Constantes.FECHA_VACIA,
									Constantes.STRING_VACIO,
									Constantes.STRING_VACIO,
									Constantes.ENTERO_CERO,
									Constantes.ENTERO_CERO };
			loggerSAFI.info(parametrosAuditoriaBean.getOrigenDatos()+"-"+"call SUBCTACAJERODIVCON(  " + Arrays.toString(parametros) + ")");
		List matches= ((JdbcTemplate)conexionOrigenDatosBean.getOrigenDatosMapa().get(parametrosAuditoriaBean.getOrigenDatos())).query(query,parametros  ,new RowMapper() {
				public Object mapRow(ResultSet resultSet, int rowNum)
						throws SQLException {
					SubCtaSucursDivBean subCtaSucursDiv = new SubCtaSucursDivBean();
					subCtaSucursDiv.setConceptoMonID(resultSet.getString(1));
					subCtaSucursDiv.setSucursalID(resultSet.getString(2));
					subCtaSucursDiv.setSubCuenta(resultSet.getString(3));

					return subCtaSucursDiv;
				}
			});
			consultaBean= matches.size() > 0 ? (SubCtaSucursDivBean) matches.get(0) : null;
		}catch(Exception e){
			e.printStackTrace();
			loggerSAFI.error(parametrosAuditoriaBean.getOrigenDatos()+"-"+"Error en la consulta de la Subcuenta por Sucursal", e);
		}
		return consultaBean;
	}


