package originacion.bean;

public class FrecuenciaBean {
	private String producCreditoID;
	private String frecuenciaID;
	private String orden;
	private String dias;
	private String descSingular;
	private String descPlural;
	private String descInfinitivo;
	public String getProducCreditoID() {
		return producCreditoID;
	}
	public void setProducCreditoID(String producCreditoID) {
		this.producCreditoID = producCreditoID;
	}
	public String getFrecuenciaID() {
		return frecuenciaID;
	}
	public void setFrecuenciaID(String frecuenciaID) {
		this.frecuenciaID = frecuenciaID;
	}
	public String getOrden() {
		return orden;
	}
	public void setOrden(String orden) {
		this.orden = orden;
	}
	public String getDias() {
		return dias;
	}
	public void setDias(String dias) {
		this.dias = dias;
	}
	public String getDescSingular() {
		return descSingular;
	}
	public void setDescSingular(String descSingular) {
		this.descSingular = descSingular;
	}
	public String getDescPlural() {
		return descPlural;
	}
	public void setDescPlural(String descPlural) {
		this.descPlural = descPlural;
	}
	public String getDescInfinitivo() {
		return descInfinitivo;
	}
	public void setDescInfinitivo(String descInfinitivo) {
		this.descInfinitivo = descInfinitivo;
	}

}
