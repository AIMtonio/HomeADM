package fondeador.bean;

import general.bean.BaseBean;

public class SubCtaMonFonBean extends BaseBean {
	private String conceptoFonID;
	private String tipoFondeo;
	private String monedaID;
	private String subCuenta;
	public String getConceptoFonID() {
		return conceptoFonID;
	}
	public void setConceptoFonID(String conceptoFonID) {
		this.conceptoFonID = conceptoFonID;
	}
	public String getTipoFondeo() {
		return tipoFondeo;
	}
	public void setTipoFondeo(String tipoFondeo) {
		this.tipoFondeo = tipoFondeo;
	}
	public String getMonedaID() {
		return monedaID;
	}
	public void setMonedaID(String monedaID) {
		this.monedaID = monedaID;
	}
	public String getSubCuenta() {
		return subCuenta;
	}
	public void setSubCuenta(String subCuenta) {
		this.subCuenta = subCuenta;
	}
	
	
	
}
