package soporte.bean;

public class CierreGeneralBean {
	private String confirmaCierre;
	private String msjValidacion;
	private String fechaCierreAnt;
	private String fechaHoraUltCie;
	private String tiempoUltimoCierre;
	
	public String getConfirmaCierre() {
		return confirmaCierre;
	}
	public void setConfirmaCierre(String confirmaCierre) {
		this.confirmaCierre = confirmaCierre;
	}
	public String getMsjValidacion() {
		return msjValidacion;
	}
	public void setMsjValidacion(String msjValidacion) {
		this.msjValidacion = msjValidacion;
	}
	public String getFechaCierreAnt() {
		return fechaCierreAnt;
	}
	public void setFechaCierreAnt(String fechaCierreAnt) {
		this.fechaCierreAnt = fechaCierreAnt;
	}
	public String getFechaHoraUltCie() {
		return fechaHoraUltCie;
	}
	public void setFechaHoraUltCie(String fechaHoraUltCie) {
		this.fechaHoraUltCie = fechaHoraUltCie;
	}
	public String getTiempoUltimoCierre() {
		return tiempoUltimoCierre;
	}
	public void setTiempoUltimoCierre(String tiempoUltimoCierre) {
		this.tiempoUltimoCierre = tiempoUltimoCierre;
	}
	

}
